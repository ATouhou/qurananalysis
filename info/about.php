	<b>
	Quran Analysis (QA) is an endeavor to build a full Semantic Search and Intelligence System for the Quran.
	</b>


<p>
<p class='info-page-text'>

	QA is a smart search engine for the Quran, where Users and Scholars can search the Quran by "meaning" using both Arabic and English languages,
	explore the Quran using state-of-art visualization techniques,
	ask questions in natural language,
	acquire detailed statistics and
	find hidden patterns and relations.
	  

<p>

<p class='info-page-text'>

	QA started as an MSc project at the <a target="_blank" href='http://www.leeds.ac.uk/'>University of Leeds</a> in 2015 supervised by <a target="_blank" href='http://www.comp.leeds.ac.uk/eric/'>Eric Atwell</a>.
	The project aimed to glu-together and build on previous research done in the university 
	and to provide an opensource base for Quran Analysis work, paving the way for innovation in this area.
	  

