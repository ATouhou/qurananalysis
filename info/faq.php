

<p class='info-page-text'>
<h1 class='info-page-section-title'>How Smart is it?</h1> 

QA offers exclusive and unique features that makes searching and exploring the Quran much easier. Through QA you can:
<ol>
<li>Ask Questions in English and Arabic</li>
<li>Explore the Quran by topics</li>
<li>Search by meaning, for example "colors" will show verses containing "green"</li>
<li>Search for word occurrence in verses, even if it is mentioned only as a pronoun</li>
<li>A Navigatable Graph is shown for all other words related to the word you searched for</li>
<li>Google-like suggestions for wrong query words</li>
<li>Your query is enriched by adding synonyms to get more results (ex: adding Paradise for word Heaven)</li>

</ol>
</p>

<p class='info-page-text'>

<h1 class='info-page-section-title'>Which Translation is used for the Quran</h1> 

The translation used for the Quran in this website is <a target="_new" href='http://www.saheehinternational.com/'>Sahih International</a>
</p>

<p class='info-page-text'>

<h1 class='info-page-section-title'>How Relevance is calculated</h1> 

The following three factors are used to calculate the relevance score for the result verses

<ol>
<li>Number of query words (and relevant pronouns) found in the verse</li>
<li>Number of times the terms appear in verse</li>
<li>Whether the whole query phrase is found in the verse</li>

</ol>

</p>

<p class='info-page-text'>

<h1 class='info-page-section-title'>Question Answering Accuracy</h1> 

So far the accuracy is 33% for answering questions in English language. However this percentage is expected to be higher in the coming months after releasing the new ontology.
